var newWindow;
var w, h = 0;
var width = window.innerWidth;
var height = window.innerHeight;

function openHaegin() {
    newWindow = open("haegin.html", "Haegin", "width=740, height=580");
    w = width / 2;
    h = height / 2;
    newWindow.moveBy(w-300, h-200);
    newWindow.focus();
}

